package LearnOperator

import "fmt"

// UseRelation  关系运算符
func UseRelation() {
	a := 2
	b := 2
	fmt.Println("a=", a, "b=", b)
	fmt.Println("a=b", a == b)
	fmt.Println("a!=b", a != b)
	fmt.Println("a>b", a > b)
	fmt.Println("a<b", a < b)
	fmt.Println("a>=b", a >= b)
	fmt.Println("a<=b", a <= b)

}
